#ifndef SRC_S21_MATRIX_H_
#define SRC_S21_MATRIX_H_

#include <stdio.h>
#include <stdlib.h>
#include <math.h>

#define OK 0 // ОК
#define INCORRECT_MATRIX 1 // Ошибка, некорректная матрица
#define ERROR 1
#define CALCULATION_ERROR 2 // Ошибка вычисления

// Для сравнения
#define SUCCESS 1 // Четко
#define FAILURE 0 // БАН

typedef struct matrix_struct {
    double **matrix;
    int rows;
    int columns;
} matrix_t;

#include "core/s21_helpers.h"

int s21_create_matrix(int rows, int columns, matrix_t *result);

/**
 * @brief Функция для очистки памяти, выделенной под матрицу.
 * 
 * Пример использования:
 * 
 * matrix A;
 * 
 * s21_remove_matrix(&A);
 * 
 * @param *A Указатель на матрицу, с которой работаем.
 */
void s21_remove_matrix(matrix_t *A);

/**
 * @brief Проверяет равенство двух матриц A и B с учётом допустимой погрешности.
 *
 * @param A Указатель на первую матрицу.
 * @param B Указатель на вторую матрицу.
 *
 * @return Возвращает:
 *         - SUCCESS (1), если матрицы равны с точностью до epsilon;
 *         - FAILURE (0), если матрицы не равны или хотя бы одна из них некорректна.
 *
 * @note Сравнение производится поэлементно с учетом погрешности (epsilon = 1e-7).
 *       Для определения равенства используется выражение: fabs(A[i][j] - B[i][j]) <= epsilon.
 *       Размеры матриц должны совпадать; иначе результат может быть некорректным.
 */
int s21_eq_matrix(matrix_t *A, matrix_t *B);

/**
 * @brief Складывает две матрицы одинакового размера и сохраняет результат в третьей матрице.
 *
 * Функция проверяет валидность входных матриц A и B, а также совпадение их размеров.
 * Если размеры совпадают и матрицы корректны, результат сложения записывается в result.
 *
 * @param A Указатель на первую матрицу.
 * @param B Указатель на вторую матрицу.
 * @param result Указатель на матрицу, в которую будет записан результат сложения.
 * @return
 *     - OK (0), если операция прошла успешно;
 *     - ERROR (1), если одна из матриц некорректна;
 *     - CALCULATION_ERROR (2), если размеры матриц не совпадают.
 */
int s21_sum_matrix(matrix_t *A, matrix_t *B, matrix_t *result);


/**
 * @brief Вычитает одну матрицу из другой и сохраняет результат в третьей матрице.
 *
 * Функция проверяет корректность входных матриц A и B, а также совпадение их размеров.
 * Если размеры совпадают и матрицы валидны, из A вычитается B, а результат сохраняется в result.
 *
 * @param A Указатель на первую матрицу (уменьшаемое).
 * @param B Указатель на вторую матрицу (вычитаемое).
 * @param result Указатель на матрицу, в которую будет записан результат вычитания.
 * @return
 *     - OK (0), если операция прошла успешно;
 *     - ERROR (1), если одна из матриц некорректна;
 *     - CALCULATION_ERROR (2), если размеры матриц не совпадают.
 */
int s21_sub_matrix(matrix_t *A, matrix_t *B, matrix_t *result);

/**
 * @brief Умножает матрицу на число.
 *
 * Выполняет поэлементное умножение всех элементов матрицы A на заданное число
 * и сохраняет результат в матрицу result.
 * Перед выполнением проверяется корректность исходной матрицы.
 * Матрица result создаётся внутри функции — важно освободить её после использования.
 *
 * @param A указатель на исходную матрицу
 * @param number число, на которое умножается матрица
 * @param result указатель на результирующую матрицу
 * @return OK в случае успешного выполнения, ERROR если матрица A некорректна или не удалось выделить память
 */
int s21_mult_number(matrix_t *A, double number, matrix_t *result);

/**
 * @brief Выполняет умножение двух матриц.
 *
 * Функция перемножает две корректные матрицы A и B по стандартному правилу линейной алгебры.
 * Результат сохраняется в матрицу result, которая создаётся внутри функции.
 * Перед выполнением проверяется корректность входных матриц, а также их размеры на совместимость:
 * число столбцов матрицы A должно быть равно числу строк матрицы B.
 *
 * @param A указатель на первую матрицу (левый множитель)
 * @param B указатель на вторую матрицу (правый множитель)
 * @param result указатель на результирующую матрицу
 * @return OK при успешном выполнении,
 *         ERROR если входные матрицы некорректны или не удалось создать результат,
 *         CALCULATION_ERROR при несовместимых размерах A и B
 */
int s21_mult_matrix(matrix_t *A, matrix_t *B, matrix_t *result);

/**
 * @brief Транспонирует заданную матрицу.
 *
 * Создаёт транспонированную копию матрицы A, записывая результат в result.
 * Перед выполнением проверяет валидность исходной матрицы.
 *
 * @param A указатель на исходную матрицу
 * @param result указатель на матрицу, в которую будет записан результат транспонирования
 * @return OK при успешном выполнении,
 *         ERROR при некорректной матрице A
 */
int s21_transpose(matrix_t *A, matrix_t *result);

/**
 * @brief Вычисляет матрицу алгебраических дополнений (матрицу кофакторов) для заданной квадратной матрицы.
 *
 * Алгебраическое дополнение элемента определяется как определитель соответствующего минора,
 * умноженный на (-1)^(i+j), где i и j — индексы элемента.
 *
 * @param A Указатель на исходную квадратную матрицу.
 * @param result Указатель на матрицу, в которую будет записан результат.
 * 
 * @return Возвращает:
 *         - OK (0) при успешном вычислении,
 *         - CALCULATION_ERROR, если матрица не квадратная,
 *         - ERROR, если указатель на матрицу невалиден.
 */
int s21_calc_complements(matrix_t *A, matrix_t *result);

/**
 * @brief Вычисляет определитель квадратной матрицы.
 *
 * Функция рекурсивно вычисляет определитель матрицы с использованием метода разложения по первой строке.
 * Поддерживаются матрицы размером 1×1, 2×2 и N×N (N > 2).
 *
 * @param A Указатель на исходную квадратную матрицу.
 * @param result Указатель на переменную, в которую будет записан результат (определитель).
 * @return OK (0), если операция прошла успешно. 
 *         CALCULATION_ERROR (2), если матрица не квадратная.
 *         ERROR (1), если передана невалидная матрица.
 */
int s21_determinant(matrix_t *A, double *result);

int s21_inverse_matrix(matrix_t *A, matrix_t *result);


#endif  